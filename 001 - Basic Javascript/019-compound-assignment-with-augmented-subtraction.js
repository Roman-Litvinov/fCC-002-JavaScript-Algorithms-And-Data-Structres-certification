/*  COMPOUND ASSIGNMENT - AUGMENTED SUBTRACTION
  Operator -= performs 2 things in one step:
  1 - it subtracts a value from the contents of a variable,
  2 - it assigns the result as the new value of that variable,
  like so: */

var someVar = 13;
// subtract 7 from someVar's current value of 13, get 6, and assign 6
// to someVar as its new value:
someVar -= 7;
console.log("Just to check, someVar is now "+someVar+".");
