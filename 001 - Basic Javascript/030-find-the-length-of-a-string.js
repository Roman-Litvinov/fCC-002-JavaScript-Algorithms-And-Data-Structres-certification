// To find a length of a string value we use 
// the .length property of a string object, like so:

var exampleStr = "This is some string.";
console.log("The length of the string value of exampleStr variable is "+exampleStr.length+".");

// we can do it directly with the string literal, like so:
console.log("The length of the string literal \"Boo!!!\" is " + 
"Boo!!!".length+".");

// Setup
var lastNameLength = 0;
var lastName = "Lovelace";

// get the length of lastName var and save it in lastNameLength var:
lastNameLength = lastName.length;
console.log("The length of lastName var is "+lastNameLength+ ".");
