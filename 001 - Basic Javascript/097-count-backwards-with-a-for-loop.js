// push the even numbers from 10 to 2 to evenArray,
// in descending order, using a FOR loop:
var evenArray = [];
for (let i = 10; i >= 2; i -= 2) {
  evenArray.push(i);
  console.log(evenArray);
}
console.log(evenArray);


// push the odd numbers from 9 to 1 to myArray,
// in descending order, using a FOR loop:
var myArray = [];
for (let i = 9; i >= 1; i -= 2) {
  myArray.push(i);
}
console.log(myArray);