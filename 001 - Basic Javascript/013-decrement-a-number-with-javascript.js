/* "To decrement a variable" means "to reduce the value of a variable by 1".
  This can be done by using the decrementation operator --. 
  So instead of doing this:
  
    myVar = myVar - 1;
  
  we do this:
    myVar--;

  which is shorter and saves space and typing time and stuff. */

var myVar = 11;

// Only change code below this line
myVar--;

console.log("myVar used to be "+(myVar+1)+", but after being decremented, it became "+myVar+".");
