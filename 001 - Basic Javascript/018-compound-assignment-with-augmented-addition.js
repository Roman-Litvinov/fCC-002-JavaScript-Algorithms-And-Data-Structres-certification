/*  COMPOUND ASSIGNMENT - AUGMENTED ADDITION
  Operator += performs 2 things in one step:
  1 - it adds a value to the contents of a variable,
  2 - it assigns the result as the new value of that variable,
  like so: */

var someVar = 3;
// add 7 to someVar's current value of 3, get 10, and assign 10 
// to someVar as its new value:
someVar += 7;
console.log("Just to check, someVar is now "+someVar+".");
